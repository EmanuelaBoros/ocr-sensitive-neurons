def categorize_layer(layer_idx, total_layers):
    """Categorizes layer into first, middle, or last based on its index."""
    if layer_idx < total_layers // 3:
        return "First_Layers"
    elif layer_idx < 2 * (total_layers // 3):
        return "Middle_Layers"
    else:
        return "Last_Layers"


